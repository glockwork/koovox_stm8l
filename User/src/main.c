#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "koovox_uart.h"
#include "koovox_message_handle.h"
#include "koovox_lis3dh_sensor.h"
#include "koovox_hb_sensor.h"

int main( void )
{
	uint32_t time = 0;

	/*********** 初始化内部系统时钟 **********/
    CLK_HSICmd(ENABLE);//开始内部高频RC
    CLK_SYSCLKSourceConfig(CLK_SYSCLKSource_HSI);//HSI为系统时钟
    CLK_SYSCLKDivConfig(CLK_SYSCLKDiv_4);	/* 8Mhz 主频 */

	enableInterrupts();	

	/******** 初始化串口 ****************/
	Init_usart1(19200);

	/*********** 初始化hb_sensor ***************/
	GPIO_Init(GPIOB, GPIO_Pin_2, GPIO_Mode_Out_PP_Low_Slow);

	/*********** I2C 初始化 ***************/
	LIS3DH_Init();

	/**************** 启动计时定时器 ***************/
	Koovox_enable_time_counter();
	
	while(1)
	{
	
		// 接收串口数据
		Koovox_receive_message();
		
		// 加速度相关计算
		Koovox_calc_accelerate();

		// 发送hb_sensor原始数据到CSR8670
		Koovox_send_hb_adc_value();

		// 心跳包唤醒csr8670
		if(curr_time - time >= 30)
		{
			time = curr_time;
			Koovox_fill_and_send_packet(ENV , SYSTEM, 0, 0);
		}

	}
}





